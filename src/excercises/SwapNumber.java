package excercises;

public class SwapNumber {
	public static void main(String[] args) {
		
		int a, b, c;
		
		 a = 10;
		 b = 20;
		 System.out.println("Value of a:"+a +" Value of b:"+b);
		 c = a;
		 a = b;
		 b = c;
		 System.out.println("After Swaping");
		 System.out.println("Value of a:"+a +" Value of b:"+b);
		
		
		
		
	}

}
